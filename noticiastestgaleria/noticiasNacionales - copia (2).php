                     LA SELECCIÓN VENEZOLANA DE SOFTBOL CONSIGUE EL ORO EN LOS JUEGOS CENTROAMERICANOS Y DEL CARIBE 2014
- Venezuela gana la final de softbol y obtiene el primer lugar con su séptima victoria consecutiva en los juegos centroamericanos y del caribe 2014, tras vencer al equipo mexicano una rayita a cero. 

<br /><br />

- El equipo local México a primera hora enfrento a la selección de Cuba en el juego que daría el pase a la final para enfrentar a la selección venezolana que llegaba invicta por la medalla de oro en estos juegos Centroamericanos y del Caribe que se desarrollan en Veracruz. Los aztecas lograron el triunfo 1-0 en un juego bastante apretado que se extendió a ocho innings cuando México anotaba la carrera del triunfo. El lanzador ganador fue Samuel Villalvazo y el lanzador perdedor el zurdo Alain Roman.
<br /><br />
JUEGO POR LA MEDALLA DE ORO.
<br /><br />
- La guerrilla vinotinto tomaba el papel de visitante en busca del juego ganado enfrentando a México en la justa del caribe, Venezuela anoto la única carrera del encuentro en el segundo inning cuando Yeider Chirinos llegaba hasta la segunda almohadilla producto de un doble, seguidamente con elevado al RF de Luiger Pinto avanzaba a la tercera base  y con error en el tiro anotaba sin problemas el shortstop de la selección criolla.
<br /><br />
- Ramón Jones, apodado ¨El Extraterrestre¨  pues su desenvolvimiento en la tabla de lanzar parece fuera de este mundo; esta oportunidad no fue la excepción pues busco e hizo su mayor esfuerzo por realizar otro no hit no run, después de que sacara a 12 bateadores seguidos, el tercera base William Prishker le conecto un duro hit al left fielder para iniciar un inning de amenaza que causaba México que con las dos únicas bases por bolas que dio Jones ponía el inning en bases llenas sin out; puso calma y logro sacar el cero dominando con rolling al segunda base y continuamente dos ponches utilizando el lanzamiento en drop. Su actuación culmino con un rolling a él para tirar a segunda y hacer una doble matanza logrando el out 21.  Concluyo con 7.0 Inning  3 hits cero carreras, no hubo errores entrego 2 bases por bolas y 8 fueron despachados por la vía de los tres strikes. Samuel Villalvazo fue el perdedor con una buena actuación en 6.0 Inning 6 hits 1 base por bolas aceptando la carrera de Venezuela.

<br /><br />

LO IMPRESIONANTE
<br /><br />
- Jones en 26.0 innings solo permitió cuatro hits, uno en contra de Panamá y tres en este encuentro por la medalla de Oro, abanico a 35, protagonizo dos juegos sin hits ni carreras en contra de República Dominica y Cuba, también hizo una labor de cerrador impresionante vs Panamá, Guatemala y Cuba en la ronda eliminatoria.
<br /><br />

-  Con la selección nacional sumo tres no hit no run durante el año, el primero en la final de los clasificatorios a estos juegos CAC a su contrincante República Dominicana, el segundo nuevamente a Dominicana el pasado 18 de noviembre, el ultimo ayer a Cuba para pasar a la final.
<br /><br />
- En el mejor torneo del mundo International Softball Congress ISC también sumo otro más al equipo Kitchener ON Bell, Jones defendía la camiseta de Pensilvania Power, en ese torneo culmino ranqueado como el tercer lanzador del mundo detrás del nueva zelandés Jeremy Manley y el Australiano Adam Folkard.  
<br /><br />
- La selección de Venezuela termino con 10 juegos ganados seguidos, 7 en los CAC y los tres últimos en el Panamericano de Softbol, tres títulos en total sumo la Guerrilla Vinotinto para el año 2014. – Clasificatorios a los CAC Cali, Colombia. – Panamericano de Softbol Paraná, Argentina – Juegos Centroamericanos y del Caribe Veracruz, México.

<br /><br />

-  De esta manera los favoritos a ganar el oro cumplieron con los pronósticos y expectativas de sus seguidores, la armonía del equipo se hizo sentir e incluso fue el motivo de esta máxima entrega por parte de cada jugador. El objetivo era quedar campeón y lo lograron de manera invicta con una actuación impecable y por supuesto, con la ayuda del insuperable talento de nuestros lanzadores venezolanos. Un logro más para el softbol criollo, lo cual es sinónimo de motivación y satisfacción para quienes admiran y forman parte de este gran deporte que poco a poco constata su avance en el país. 

